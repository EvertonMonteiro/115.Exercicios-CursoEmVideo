lar = float(input('Qual a largura da parede? '))
alt = float(input('Qual a altura da parede? '))
are = lar*alt
print(f'Considerando que sua parede tem {lar} de largura e {alt} de altura \na sua área equivale a {are}m²')
tin = are/2
print(f'Para pintar essa parede será necessário {tin} litros de tinta')